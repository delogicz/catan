package jsonstructures;

/**
 * Class for Ertrag from other players 
 * @author Felixi, Georg
 *
 */
public class ErtragUnbekanntDaten {
	private int Spieler;
	private RohstoffUnbekanntDaten Rohstoffe;
	
	/**
	 * Constructor
	 * @param spieler
	 * @param anzahl
	 */
	public ErtragUnbekanntDaten(int spieler, int anzahl) {
		super();
		Spieler = spieler;
		Rohstoffe = new RohstoffUnbekanntDaten(anzahl);
	}
	
	/**
	 * Getter for Spieler
	 * @return
	 */
	public int getSpieler() {
		return Spieler;
	}
	
	/**
	 * Getter for Rohstoffe
	 * @return
	 */
	public RohstoffUnbekanntDaten getRohstoffe() {
		return Rohstoffe;
	}
	
	
}

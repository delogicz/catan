package jsonstructures;

/**
 * This class is the wrapper for the idDaten class send by the server
 * @author Felixi, Georg
 *
 */
public class WillkommenNachricht {
	private idDaten Willkommen;
	
	/**
	 * Creates a idDaten object 
	 * @param id		id of the player/client
	 */
	public WillkommenNachricht(int id){
		Willkommen = new idDaten(id);
	}
	
	/**
	 * Getter for idDaten
	 * @return
	 */
	public int getDaten(){
		return Willkommen.getDaten();
	}
}

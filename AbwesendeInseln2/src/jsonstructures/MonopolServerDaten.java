package jsonstructures;
/**
 * Data of Server data for Monopol dev card
 * @author Felixi, Georg
 *
 */
public class MonopolServerDaten {
	String Rohstoff;
	int Spieler;
	
	/**
	 * Constructor
	 * @param rohstoff
	 * @param spieler
	 */
	public MonopolServerDaten(String rohstoff, int spieler) {
		Rohstoff = rohstoff;
		Spieler = spieler;
	}

	/**
	 * Getter for Rohstoff
	 * @return
	 */
	public String getRohstoff() {
		return ConvertMaster.convertToResource(Rohstoff).toString();
	}

	/**
	 * Getter for Spieler
	 * @return
	 */
	public int getSpieler() {
		return Spieler;
	}
	
	
}

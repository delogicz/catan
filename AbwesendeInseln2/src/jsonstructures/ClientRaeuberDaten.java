package jsonstructures;

/**
 * This class handles when the client displaces the robber and steals from someone, for the server
 * @author Felixi, Georg
 *
 */
public class ClientRaeuberDaten {
	OrtDaten Ort;
	int Ziel;
	
	/**
	 * Initializes ClientRaeuberDaten
	 * @param ort
	 * @param ziel
	 */
	
	public ClientRaeuberDaten(String ort, int ziel) {
		Ort = new OrtDaten(ort);
		Ziel = ziel;
	}
	
	/**
	 * Getter for Ort
	 * @return
	 */
	public OrtDaten getOrt() {
		return Ort;
	}
	
	/**
	 * Setter for Ort
	 * @param ort
	 */
	public void setOrt(String ort) {
		Ort = new OrtDaten(ort);
	}
	
	/**
	 * Getter for Ziel
	 * @return
	 */
	public int getZiel() {
		return Ziel;
	}
	
	/**
	 * Setter for Ziel
	 * @param ziel
	 */
	public void setZiel(int ziel) {
		Ziel = ziel;
	}
	
}
